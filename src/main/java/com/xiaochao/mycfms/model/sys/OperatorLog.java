package com.xiaochao.mycfms.model.sys;

public class OperatorLog {
    private Integer id;

    private String optNo;

    private String optName;

    private String deptName;

    private String smentNo;

    private String optObject;

    private String optDate;

    private String optTime;

    private String optResult;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getOptNo() {
        return optNo;
    }

    public void setOptNo(String optNo) {
        this.optNo = optNo;
    }

    public String getOptName() {
        return optName;
    }

    public void setOptName(String optName) {
        this.optName = optName;
    }

    public String getDeptName() {
        return deptName;
    }

    public void setDeptName(String deptName) {
        this.deptName = deptName;
    }

    public String getSmentNo() {
        return smentNo;
    }

    public void setSmentNo(String smentNo) {
        this.smentNo = smentNo;
    }

    public String getOptObject() {
        return optObject;
    }

    public void setOptObject(String optObject) {
        this.optObject = optObject;
    }

    public String getOptDate() {
        return optDate;
    }

    public void setOptDate(String optDate) {
        this.optDate = optDate;
    }

    public String getOptTime() {
        return optTime;
    }

    public void setOptTime(String optTime) {
        this.optTime = optTime;
    }

    public String getOptResult() {
        return optResult;
    }

    public void setOptResult(String optResult) {
        this.optResult = optResult;
    }
}